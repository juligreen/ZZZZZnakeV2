package de.is2.ZZZZZnake.Utility;

public class TwoPartVector {
    private int x;
    private int y;

    public void setX(int value) {
        x = value;
    }

    public void setY(int value) {
        y = value;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
